_INDEX_QUERY_SECTIONS = [
    ("In flight", "stage IN ('materials_drafted', 'prep_in_progress')", "created_at DESC"),
    ("Applied", "stage IN ('applied', 'interview', 'offer')", "COALESCE(stage_updated, created_at) DESC"),
    ("Waitlisted", "stage = 'waitlisted'", "created_at DESC"),
]
_REJECTED_CLAUSE = "stage IN ('rejected', 'not_selected')"
_PER_SECTION_CAP = 50


def _fetch_section(db: sqlite3.Connection, where: str, order: str) -> list[sqlite3.Row]:
    return db.execute(
        f"SELECT fingerprint, title, company, stage, fit_score, created_at, stage_updated "
        f"FROM jobs WHERE {where} ORDER BY {order} LIMIT {_PER_SECTION_CAP + 1}"
    ).fetchall()


@router.get("/materials/", response_class=HTMLResponse)
def materials_index(
    request: Request,
    db: sqlite3.Connection = Depends(get_db),  # noqa: B008
    regen_error: str = "",
) -> HTMLResponse:
    sections = []
    for name, where, order in _INDEX_QUERY_SECTIONS:
        rows = _fetch_section(db, where, order)
        overflow = len(rows) > _PER_SECTION_CAP
        sections.append({"name": name, "rows": rows[:_PER_SECTION_CAP], "overflow": overflow})
    rejected_rows = _fetch_section(db, _REJECTED_CLAUSE, "created_at DESC")
    rejected = {
        "rows": rejected_rows[:_PER_SECTION_CAP],
        "overflow": len(rejected_rows) > _PER_SECTION_CAP,
        "count": len(rejected_rows) if len(rejected_rows) <= _PER_SECTION_CAP else f"{_PER_SECTION_CAP}+",
    }
    templates = request.app.state.templates
    return templates.TemplateResponse(
        request=request,
        name="materials/index.html",
        context={"sections": sections, "rejected": rejected, "regen_error": regen_error},
    )


def _resolve_folder_with_speculative_fallback(fingerprint: str, db: sqlite3.Connection, root: Path) -> Path | None:
    """resolve_folder + synthetic-row fallback to speculative_briefing_folder.

    Synthetic rows have no prep_folder_path until flag-for-prep, but the
    speculative briefing folder exists on disk from approve-time. Both the
    folder index and the per-file route need to reach into it; keeping the
    fallback in one place avoids the drift that produced #589 (folder index
    rendered the speculative briefing row, single-file route 404'd).

    Returns None when the row has no usable folder. Callers decide UX:
    folder_view redirects synthetic-no-folder rows to /jd, file_serve 404s.
    """
    folder = resolve_folder(fingerprint, db, root)
    if folder is not None:
        return folder
    synth_row = db.execute(
        "SELECT synthetic, speculative_briefing_folder FROM jobs WHERE fingerprint = ?",
        (fingerprint,),
    ).fetchone()
    if synth_row is None or not synth_row["synthetic"]:
        return None
    spec_folder_name = synth_row["speculative_briefing_folder"]
    if not spec_folder_name:
        return None
    spec_folder = (root / spec_folder_name).resolve()
    try:
        spec_folder.relative_to(root.resolve())
    except ValueError:
        return None  # path-traversal guard on the stored folder name
    if not spec_folder.is_dir():
        return None
    return spec_folder


@router.get("/materials/{fingerprint}", response_class=HTMLResponse, response_model=None)
def folder_view(
    fingerprint: str,
    request: Request,
    db: sqlite3.Connection = Depends(get_db),  # noqa: B008
) -> HTMLResponse | RedirectResponse:
    from findajob.cost_rollups import per_job_breakdown

    root: Path = request.app.state.companies_root
    folder = _resolve_folder_with_speculative_fallback(fingerprint, db, root)
    if folder is None:
        # Synthetic rows with no usable briefing folder land on the role-card
        # description page; non-synthetic missing-folder is a 404.
        synth_row = db.execute(
            "SELECT synthetic FROM jobs WHERE fingerprint = ?",
            (fingerprint,),
        ).fetchone()
        if synth_row is not None and synth_row["synthetic"]:
            return RedirectResponse(url=f"/jobs/{fingerprint}/jd", status_code=303)
        raise HTTPException(status_code=404, detail="folder not found")
    row = db.execute(
        "SELECT id, title, company, stage, stage_updated FROM jobs WHERE fingerprint = ?",
        (fingerprint,),
    ).fetchone()
    title = row["title"] if row else ""
    company = row["company"] if row else ""
    groups = _group_files(folder, title=title, company=company)

    # Per-operation cost breakdown (only if cost_log exists; fail open)
    breakdown = []
    breakdown_total = None
    if row is not None:
        try:
            breakdown = per_job_breakdown(db, row["id"])
            if breakdown:
                breakdown_total = sum(r.cost_usd for r in breakdown)
        except sqlite3.OperationalError:
            # Test fixtures may lack cost_log.
            breakdown = []

    stage = row["stage"] if row else ""
    is_post_applied = stage in POST_APPLIED_STAGES
    applied_date = _latest_applied_date(folder) if is_post_applied else None

    # Briefing-first gate (#691). Load reject_reasons only when the gate is
    # active so the folder view stays cheap for the other 99% of states.
    is_briefing_gate = stage == BRIEFING_GATE_STAGE
    reject_reasons: tuple[str, ...] = ()
    if is_briefing_gate:
        from findajob.config_loader import load_reject_reasons

        try:
            reject_reasons, _ = load_reject_reasons()
        except Exception:
            # Missing / malformed yaml: the gate still renders with a
            # free-text reject input via the fallback (handled in template).
            reject_reasons = ()

    # Load flashcard data for the in-browser viewer (latest .json in the folder
    # that matches the Flashcards group pattern). Parsed into a list so Jinja's
    # |tojson filter produces safe, unambiguous output for the Alpine x-data attr.
    flashcards_data: list | None = None
    for p in sorted(folder.iterdir(), key=lambda p: p.stat().st_mtime, reverse=True):
        if p.is_file() and " Flashcards - " in p.name and p.suffix == ".json":
            try:
                flashcards_data = json.loads(p.read_text(encoding="utf-8"))
            except (OSError, json.JSONDecodeError):
                pass
            break

    # Interview prep re-run (#875). Show button when stage=interview;
    # detect in-flight task to disable re-click.
    is_interview_stage = stage == "interview"
    interview_prep_generating = False
    if is_interview_stage and row is not None:
        from findajob.background_tasks import find_active_for_subject

        interview_prep_generating = find_active_for_subject(db, row["id"], "interview_prep") is not None

    # Podcast section (#870). Detect which podcast formats have been generated
    # and build the context for the template's audio player + generate buttons.
    gemini_configured = bool(os.environ.get("GEMINI_API_KEY", "").strip())
    job_id = row["id"] if row else None
    podcast_formats = _build_podcast_context(folder, db=db, job_id=job_id) if gemini_configured else []

    templates = request.app.state.templates
    return templates.TemplateResponse(
        request=request,
        name="materials/folder.html",
        context={
            "fingerprint": fingerprint,
            "folder_name": folder.name,
            "title": title,
            "company": company,
            "stage": stage,
            "last_generated_iso": row["stage_updated"] if row else None,
            "groups": groups,
            "breakdown": breakdown,
            "breakdown_total": breakdown_total,
            "is_post_applied": is_post_applied,
            "applied_date": applied_date,
            "is_briefing_gate": is_briefing_gate,
            "reject_reasons": reject_reasons,
            "is_interview_stage": is_interview_stage,
            "interview_prep_generating": interview_prep_generating,
            "interview_rerun_error": request.query_params.get("interview_rerun_error", ""),
            "flashcards_data": flashcards_data,
            "gemini_configured": gemini_configured,
            "podcast_formats": podcast_formats,
            "podcast_error": request.query_params.get("podcast_error", ""),
        },
    )


