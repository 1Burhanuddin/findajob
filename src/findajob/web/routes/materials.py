# Document-type classification for the materials folder view. Filename
# patterns generated by prep_application.py / find_contacts.py / triage.py:
#   "JD - {Company} - {Title}.txt"             — Job Description
#   "Review Checklist - {Company} - {Title}.md"
#   "{Display} Briefing - {Company} - {Title} - {ts}.{md,docx}"
#   "{Display} Resume - {Company} - {Title} - {ts}.{md,docx}"
#   "{Display} Resume Changes - {Company} - {Title} - {ts}.md"
#   "{Display} Cover - {Company} - {Title} - {ts}.{md,docx}"
#   "{Display} Critique - {Company} - {Title} - {ts}.md"
#   "{Display} Outreach to {Recipient} - {Company} - {ts}.txt"
#   "briefing.md"                              — speculative submission folder
# The classifier checks substrings rather than parsing the full pattern so
# changes to display_name / company / title don't break grouping.
_GROUP_RULES: list[tuple[str, str, int]] = [
    # (substring/marker, label, sort order)
    ("JD - ", "Job Description", 1),
    ("briefing.md", "Briefing (speculative)", 2),
    (" Briefing - ", "Briefing", 2),
    (" Resume Changes - ", "Resume Changes", 4),
    (" Resume - ", "Resume", 3),
    (" Cover - ", "Cover Letter", 5),
    (" Outreach to ", "Outreach", 6),
    (" Critique - ", "Recruiter Critique", 7),
    ("Review Checklist - ", "Review Checklist", 8),
    (" Interview Prep - ", "Interview Prep", 9),
    (" Study Guide - ", "Study Guide", 10),
    (" Flashcards - ", "Flashcards", 11),
]
_OTHER_LABEL = "Other"
_OTHER_ORDER = 99
_OUTREACH_RECIPIENT_RE = re.compile(r" Outreach to ([^-]+?) - ")
# Hide audit-snapshot (.applied-YYYY-MM-DD.md, written on apply transition #210)
# and edit-backup (.bak, written on first edit via /materials/{fp}/files/{name})
# files from the per-folder view — they exist for audit/recovery, not day-to-day display.
_HIDE_FROM_VIEW_RE = re.compile(
    r"\.applied-\d{4}-\d{2}-\d{2}\.md$|\.bak$| Flashcards - .*\.json$|interview_prep_podcast_.*\.mp3$"
)
_APPLIED_DATE_RE = re.compile(r"\.applied-(\d{4}-\d{2}-\d{2})\.md$")

# Stages where the materials have already been sent — banner reminds the
# operator that edits don't change what the employer received.
POST_APPLIED_STAGES = frozenset({"applied", "interview", "offer", "not_selected", "withdrawn"})

# Briefing-first gate stage (#691). The folder view renders a Continue-prep
# affordance + reject dropdown when stage matches, so the operator can
# decide based on Phase A's briefing before paying for Phase B.
BRIEFING_GATE_STAGE = "briefing_ready"


def _latest_applied_date(folder: Path) -> str | None:
    """Return the most recent date (YYYY-MM-DD) seen in *.applied-DATE.md
    snapshot filenames, or None if no snapshots exist."""
    dates = []
    for p in folder.iterdir():
        if not p.is_file():
            continue
        m = _APPLIED_DATE_RE.search(p.name)
        if m:
            dates.append(m.group(1))
    return max(dates) if dates else None


